  // ── Build the tile timeline from a song ─────────────────────────────
  // Note format: single = [midi, beats]; chord = [[m1, m2, ...], beats]. A chord
  // pushes one tile per pitch sharing the same hitTime + duration, scored
  // independently — each lane is its own hit. cum advances once per note entry.
  function buildTiles(song) {
    var spb = 60 / song.bpm, cum = 0, out = [];
    var sNotes = [];
    staffKeySig = song.keySig || 0;
    staffSpb = spb;
    // pitches() flattens either form into the list of MIDI notes for that entry.
    function pitchesOf(n) {
      var first = n[0];
      return Array.isArray(first) ? first : [first];
    }
    // collect every distinct pitch in the song (across chords too) for rank mapping
    var ms = [];
    song.notes.forEach(function (n) {
      pitchesOf(n).forEach(function (m) { if (m > 0) ms.push(m); });
    });
    // Lane assignment. Default ('rank'): the distinct pitches are sorted and spread
    // evenly across the lanes, so any melody lights all 7 (a low outlier won't squash
    // the rest). Per-song keymap:'white' instead folds each pitch into its white-key
    // lane (C/C#→0, D/D#→1, E→2, F/F#→3, G/G#→4, A/A#→5, B→6) — a note always lands in
    // the same lane, like a 7-key piano; better for chromatic tunes (e.g. the Habanera).
    var uniq = ms.slice().sort(function (a, b) { return a - b; })
      .filter(function (v, i, a) { return i === 0 || v !== a[i - 1]; });
    var WHITE_LANE = [0, 0, 1, 1, 2, 3, 3, 4, 4, 5, 5, 6];
    song.notes.forEach(function (n) {
      var beats = n[1];
      pitchesOf(n).forEach(function (midi) {
        if (midi > 0) {
          var lane;
          if (song.keymap === 'white' && LANES === 7) {
            lane = WHITE_LANE[midi % 12];
          } else {
            var rank = uniq.indexOf(midi);
            lane = uniq.length > 1
              ? Math.round(rank / (uniq.length - 1) * (LANES - 1))
              : Math.floor(LANES / 2);
          }
          lane = Math.max(0, Math.min(LANES - 1, lane));
          out.push({
            lane: lane, hitTime: cum, dur: beats * spb,
            freq: midiToFreq(midi), state: 'pending',
            // black-key pitch (C#/D#/F#/G#/A#, incl. their flat spellings) → dark bar,
            // in every song — a piano-style cue for which falling notes are accidentals
            black: [1, 3, 6, 8, 10].indexOf(midi % 12) >= 0
          });
          // staff entry: pitch + start-time (s) + duration in beats (for notehead style)
          sNotes.push({ midi: midi, start: cum, durBeats: beats });
        }
      });
      cum += beats * spb;
    });
    staffNotes = sNotes;
    // For each tile, the time (s) until the NEXT tile in the SAME lane. The renderer
    // caps a tile's drawn height to this gap so stacked same-lane tiles never overlap,
    // however fast the run — keeping dense melodies (e.g. Pulsar's sixteenths) readable
    // without changing any timing.
    var lastByLane = {};
    for (var qi = out.length - 1; qi >= 0; qi--) {
      var qt = out[qi];
      qt.gapNext = (lastByLane[qt.lane] === undefined) ? Infinity
                 : (lastByLane[qt.lane] - qt.hitTime);
      lastByLane[qt.lane] = qt.hitTime;
    }
    tiles = out; totalTiles = out.length;
    songLen = cum;
  }

  // Reset the Joker suit state at the start of a run.
  function resetSuits() {
    suitQueue = []; suitCycle = 0; lastBankCombo = 0;
    fx = { x2: 0, shield: 0, grace: 0 };
  }

  // ── Start a run ─────────────────────────────────────────────────────
  function startGame() {
    audio();
    buildTiles(SONGS[chosenSong]);
    score = 0; combo = 0; bestCombo = 0; health = 100;
    resetSuits();
    // this song's best UNDER THE CURRENT SETTINGS — the HUD target. Keys/Speed/
    // Joker each change how hard (and how the score is multiplied) a run is, so
    // a best set on Gentle/4-key shouldn't masquerade as the target on Swift/7-key.
    bestScore = bestFor(SONGS[chosenSong].name, LANES, chosenDiff, jokerPower);
    nPerfect = 0; nGood = 0; nMiss = 0; processed = 0;
    gameTime = 0;   // reset so the countdown→play transition frame draws from t=0,
                    // not a leftover time from the previous run (one-frame tile jump)
    pops = []; sparks = []; laneFlash = new Array(LANES).fill(0);
    sustainEnd = new Array(LANES).fill(0); sustainTick = new Array(LANES).fill(0);
    sustainFreq = new Array(LANES).fill(0); sustainN = new Array(LANES).fill(0);
    if (elProgress) elProgress.style.width = '0%';
    lastProgressAt = 0;     // ensure the first in-loop update fires immediately
    updateHud();
    hide(menu); hide(pauseScreen); hide(endScreen);
    state = 'countdown';
    countEnd = performance.now() + 3000;
  }

  function speed() {            // px per second so a tile crosses H in `lead`
    return strikeY / DIFFS[chosenDiff].lead;
  }

  // ── Input → resolve a strike on a lane ──────────────────────────────
  function strike(lane) {
    if (state !== 'playing') return;
    laneFlash[lane] = gameTime;
    // ♠ Spades "steady hand" widens the timing windows while active
    var graceOn = jokerPower && fx.grace > gameTime;
    var gw = graceOn ? GOOD_WIN * 1.5 : GOOD_WIN;
    var pw = graceOn ? PERFECT_WIN * 1.6 : PERFECT_WIN;
    // nearest pending tile in this lane within the Good window
    var best = null, bestDt = gw + 1;
    for (var i = 0; i < tiles.length; i++) {
      var tl = tiles[i];
      if (tl.state !== 'pending' || tl.lane !== lane) continue;
      var dt = Math.abs(tl.hitTime - gameTime);
      if (dt < bestDt) { bestDt = dt; best = tl; }
    }
    if (best && bestDt <= gw) {
      var perfect = bestDt <= pw;
      best.state = 'hit';
      tone(best.freq, Math.max(0.9, best.dur * 1.4), perfect ? 1 : 0.85);
      registerHit(perfect ? HIT_PERFECT : HIT_GOOD, best.lane);
      // long note → open a whammy/sustain window on this lane while its tail falls
      if (best.dur >= SUSTAIN_MIN) {
        best.held = true;
        sustainEnd[lane] = best.hitTime + best.dur;
        sustainTick[lane] = gameTime;
        sustainFreq[lane] = best.freq; sustainN[lane] = 0;
      }
    } else {
      // In Joker mode, a suit-lane pressed in a lull spends that suit's power
      // (bestDt = how near the closest tile in this lane is — a real hit already
      // failed above, so this only fires when no tile is bearing down).
      if (jokerPower && spendSuitOnLane(lane, bestDt)) return;
      // whammy: a long note is still sustaining in this lane → extra presses
      // earn a small bonus, a tiny health trickle, and NEVER break the combo
      // (rate-limited so a single frantic mash can't farm infinite points; the
      // health trickle inherits the same gate, capping sustained heal at ~3.5
      // hp/sec — about three Good hits' worth)
      if (sustainEnd[lane] > gameTime) {
        if (gameTime - sustainTick[lane] >= WHAMMY_GAP) {
          sustainTick[lane] = gameTime;
          sustainN[lane]++;
          whammySound(sustainFreq[lane] || best && best.freq || 440, sustainN[lane]);
          score += 5 * (1 + Math.floor(combo / 10));
          health = Math.min(100, health + 0.25);
          var wx = (lane + 0.5) * laneW;
          sparks.push({ x: wx, y: strikeY, t: gameTime, col: hsl(laneHue(lane), 90, 70) });
          flashNum(elScore); updateHud();
        }
        return;
      }
      // empty / early strike — anti-mash: break the combo, soft tick
      // (♣ Clubs shield protects the combo from a mash)
      clickSound();
      if (combo > 0 && !(jokerPower && fx.shield > gameTime)) { combo = 0; lastBankCombo = 0; }
      updateHud();
    }
  }

  // Bank suits as the combo crosses each SUIT_COMBO milestone (Joker mode only).
  function bankSuits() {
    while (suitQueue.length < 4 && combo >= lastBankCombo + SUIT_COMBO) {
      lastBankCombo += SUIT_COMBO;
      var s = suitCycle % 4; suitCycle++;
      suitQueue.push(s);
      pushPop(W * 0.5, strikeY - 58, SUIT_GLYPH[s] + ' ready', SUIT_COL[s]);
    }
  }

  // Apply suit power s at screen-x x (♥ heal, ♦ ×2, ♣ shield, ♠ steady hand).
  function applySuit(s, x) {
    if (x == null) x = W * 0.5;
    var col = SUIT_COL[s];
    if (s === 0) { health = Math.min(100, health + 25); pushPop(x, strikeY - 58, '♥ +health', col); }
    else if (s === 1) { fx.x2 = gameTime + 8; pushPop(x, strikeY - 58, '♦ ×2 score', col); }
    else if (s === 2) { fx.shield = gameTime + 8; pushPop(x, strikeY - 58, '♣ shield', col); }
    else { fx.grace = gameTime + 8; pushPop(x, strikeY - 58, '♠ steady hand', col); }
    sparks.push({ x: x, y: strikeY, t: gameTime, col: col });
    announce(SUIT_NAMES[s] + ' power');
    updateHud();
  }
  // Space (desktop): spend the oldest banked suit.
  function useSuit() {
    if (!jokerPower || state !== 'playing' || suitQueue.length === 0) return;
    applySuit(suitQueue.shift());
  }
  // Press a suit's own lane to fire it — but only in a lull (nearest tile in that
  // lane > 0.45s away), so an active hit always wins and a suit is never wasted.
  function spendSuitOnLane(lane, nearestDt) {
    if (!jokerPower || state !== 'playing') return false;
    var s = laneSuit(lane); if (s < 0) return false;
    var idx = suitQueue.indexOf(s); if (idx < 0) return false;
    if (nearestDt < 0.45) return false;
    suitQueue.splice(idx, 1);
    applySuit(s, (lane + 0.5) * laneW);
    return true;
  }

  function registerHit(kind, lane) {
    processed++;
    var cx = (lane + 0.5) * laneW;
    if (kind === HIT_MISS) {
      nMiss++;
      if (jokerPower && fx.shield > gameTime) {
        // ♣ Clubs shield — the missed note doesn't break the combo or cost health
        pushPop(cx, strikeY - 26, 'Shield', '#e8cf8c');
        announce('Shielded');
      } else {
        combo = 0; lastBankCombo = 0;
        health = Math.max(0, health - DIFFS[chosenDiff].missCost);
        pushPop(cx, strikeY - 26, 'Miss', '#d06a52');
        announce('Miss');
        if (health <= 0) endRun(false);
      }
    } else {
      combo++;
      if (combo > bestCombo) bestCombo = combo;
      if (jokerPower) bankSuits();
      var mult = 1 + Math.floor(combo / 10);  // +1× every 10
      var x2 = (jokerPower && fx.x2 > gameTime) ? 2 : 1;  // ♦ Diamonds fortune (power-up)
      var keyMul = KEY_SCORE_MULT[LANES] || 1;            // more keys → more points
      var fl = feverLevel(combo);   // fever tier — denser spark bursts as combo climbs
      if (kind === HIT_PERFECT) {
        nPerfect++; score += Math.round(100 * mult * x2 * keyMul);
        health = Math.min(100, health + 3);
        pushPop(cx, strikeY - 26, 'Perfect', hsl(laneHue(lane), 90, 70));
        spawnHitSparks(cx, hsl(laneHue(lane), 90, 70), fl);
      } else {
        nGood++; score += Math.round(50 * mult * x2 * keyMul);
        health = Math.min(100, health + 1);
        spawnHitSparks(cx, hsl(laneHue(lane), 80, 64), fl);
        pushPop(cx, strikeY - 26, 'Good', '#c8bfe0');
      }
      flashNum(elScore); if (combo > 1) flashNum(elCombo);
    }
    updateHud();
  }

  function pushPop(x, y, txt, col) {
    if (reduceMotion) return;
    pops.push({ x: x, y: y, txt: txt, col: col, t: gameTime });
  }
  // A hit's spark ring(s) — extra rings stack on at higher fever tiers for a
  // denser burst. Backdating `t` slightly makes the extra rings render already
  // mid-expansion, so they read as one fuller burst rather than duplicates.
  // Gated behind reduceMotion same as the rest of the game's moving FX.
  function spawnHitSparks(cx, col, fl) {
    sparks.push({ x: cx, y: strikeY, t: gameTime, col: col });
    if (reduceMotion) return;
    if (fl >= 1) sparks.push({ x: cx, y: strikeY, t: gameTime - 0.05, col: col });
    if (fl >= 3) sparks.push({ x: cx, y: strikeY, t: gameTime - 0.1, col: col });
  }
  function announce(msg) { elSr.textContent = msg; }

  // ── HUD ─────────────────────────────────────────────────────────────
  function updateHud() {
    elScore.textContent = score;
    elBest.textContent = bestScore;
    elCombo.textContent = combo;
    var done = nPerfect + nGood + nMiss;
    var acc = done ? Math.round((nPerfect + nGood) / done * 100) : 100;
    elAcc.textContent = acc + '%';
    elHealth.style.width = health + '%';
    elHealth.classList.toggle('low', health <= 30);
    // Critical health (≤15, mid-run only): pulse the stage's existing inset
    // crimson glow harder as an urgency cue that doesn't rely on the player
    // watching the thin health bar. CSS handles the pulse (and a static
    // fallback under prefers-reduced-motion) — see .stage.crit.
    if (stageEl) stageEl.classList.toggle('crit', state === 'playing' && health <= 15);
    updateSuits();
  }
  // Show the banked suits as their actual glyphs (oldest left = next Space spends);
  // empty slots show a faint dot.
  function updateSuits() {
    if (elSuitCell) elSuitCell.style.display = jokerPower ? '' : 'none';
    if (!jokerPower) return;
    for (var i = 0; i < elSuitPips.length; i++) {
      var pip = elSuitPips[i];
      if (i < suitQueue.length) {
        var s = suitQueue[i];
        pip.textContent = SUIT_GLYPH[s];
        pip.className = 'ready' + (i === 0 ? ' spend-next' : '');
        pip.style.color = SUIT_COL[s];
        pip.style.textShadow = '0 0 10px ' + SUIT_COL[s];
      } else {
        pip.textContent = '·';
        pip.className = '';
        pip.style.color = '';
        pip.style.textShadow = '';
      }
    }
  }
  function flashNum(el) {
    if (reduceMotion) return;
    el.classList.add('flash');
    setTimeout(function () { el.classList.remove('flash'); }, 150);
  }

  // ── End of run ──────────────────────────────────────────────────────
  function endRun(won) {
    state = won ? 'win' : 'over';
    var done = nPerfect + nGood + nMiss;
    var acc = done ? Math.round((nPerfect + nGood) / done * 100) : 0;
    var stars = won ? (acc >= 95 ? 3 : acc >= 80 ? 2 : acc >= 55 ? 1 : 0) : 0;
    endAcc = acc; endStars = stars;       // remember for the share text
    var key = SONGS[chosenSong].name;
    var sk = settingsKey(LANES, chosenDiff, jokerPower);
    var prevRec = bestRecordFor(key, LANES, chosenDiff, jokerPower);
    endNewBest = score > prevRec.score;   // for the share text — did THIS run set the high score
    // Score and stars are tracked as INDEPENDENT running maxima, not a single
    // {score, stars} pair from one attempt — they measure different things
    // (score rewards combo/risk, stars rewards accuracy), so your best-ever
    // accuracy grade on a song shouldn't be lost just because a separate,
    // sloppier-but-higher-combo run holds the score record. This also matters
    // for the Ouroboros unlock below: a 3-star run that doesn't ALSO beat the
    // score record must still count, or the unlock could silently never fire.
    var wasThreeStarred = hasThreeStarSong();
    var bestScoreEver = Math.max(prevRec.score, score);
    var bestStarsEver = Math.max(prevRec.stars, stars);
    if (bestScoreEver !== prevRec.score || bestStarsEver !== prevRec.stars) {
      if (!bests[key]) bests[key] = {};
      bests[key][sk] = { score: bestScoreEver, stars: bestStarsEver };
      saveBests();
    }
    updateSnakeUnlock();
    var unlockNoteHTML = '';
    if (!wasThreeStarred && hasThreeStarSong()) {
      announce('Ouroboros unlocked — see Settings');
      unlockNoteHTML = '<br><span class="unlock-note">✦ Three stars — Ouroboros unlocked. See Settings.</span>';
    }
    bestScore = bestFor(key, LANES, chosenDiff, jokerPower);
    elBest.textContent = bestScore;
    resetShareBtn(); setShareStatus('');  // clear any leftover "Copied" state
    var song = SONGS[chosenSong];
    document.getElementById('endSong').innerHTML =
      song.name + '<span class="by">' + song.by + '</span>';
    var starEl = document.getElementById('endStars');
    var s = '';
    for (var i = 0; i < 3; i++) s += i < stars ? '★' : '<span class="off">★</span>';
    starEl.innerHTML = s;
    document.getElementById('endTitle').textContent =
      won ? (stars === 3 ? 'Flawless' : 'Melody complete') : 'The light faded';
    document.getElementById('endStats').innerHTML =
      'Score <b>' + score + '</b><br>' +
      'Accuracy <b>' + acc + '%</b> &nbsp;·&nbsp; Best combo <b>' + bestCombo + '</b><br>' +
      'Perfect <b>' + nPerfect + '</b> &nbsp; Good <b>' + nGood + '</b> &nbsp; Miss <b>' + nMiss + '</b>' +
      unlockNoteHTML;
    announce((won ? 'Melody complete. Score ' : 'Game over. Score ') + score);
    show(endScreen);
  }

  // ── Pause ───────────────────────────────────────────────────────────
  function togglePause() {
    if (state === 'playing') {
      state = 'paused'; pauseAt = performance.now(); show(pauseScreen);
    } else if (state === 'paused') {
      t0 += performance.now() - pauseAt;   // shift the clock by paused span
      state = 'playing'; hide(pauseScreen);
    }
  }
  function quitToMenu() {
    state = 'menu';
    buildSongList();        // refresh so updated per-song bests show in the list
    if (typeof setPanel === 'function') setPanel(null);   // close any open panel
    hide(pauseScreen); hide(endScreen); show(menu);
  }
  function playAgain() { startGame(); }

  function show(el) { el.classList.remove('hidden'); }
  function hide(el) { el.classList.add('hidden'); }

  // ── Main loop ───────────────────────────────────────────────────────
  function loop(now) {
    requestAnimationFrame(loop);

    if (state === 'countdown') {
      var left = countEnd - now;
      if (left <= 0) { state = 'playing'; t0 = now; }
      draw(now);
      return;
    }
    if (state === 'playing') {
      gameTime = (now - t0) / 1000;
      // song-progress strip — throttled to ~5 Hz so we don't trigger layout for
      // a one-pixel change every frame on phones. The CSS transition smooths it.
      if (elProgress && songLen > 0 && now - lastProgressAt > 200) {
        lastProgressAt = now;
        var pct = Math.max(0, Math.min(100, (gameTime / songLen) * 100));
        elProgress.style.width = pct + '%';
      }
      // mark misses for tiles whose window has fully passed
      for (var i = 0; i < tiles.length; i++) {
        var tl = tiles[i];
        if (tl.state === 'pending' && gameTime > tl.hitTime + GOOD_WIN) {
          tl.state = 'missed';
          registerHit(HIT_MISS, tl.lane);
        }
      }
      if (state === 'playing' && gameTime > songLen + 1.1) endRun(true);
    }
    draw(now);
  }

  // ── Drawing ─────────────────────────────────────────────────────────
  function draw(now) {
    ctx.clearRect(0, 0, W, H);
    var sp = speed();

    // lane separators
    ctx.strokeStyle = 'rgba(217,185,106,0.10)';
    ctx.lineWidth = 1;
    for (var l = 1; l < LANES; l++) {
      ctx.beginPath(); ctx.moveTo(l * laneW, 0); ctx.lineTo(l * laneW, H); ctx.stroke();
    }

    // strike line + lane pads
    var padTop = strikeY, padH = H - strikeY;
    // Fever: the ambient (unlit) lane glow brightens a notch per tier, a quiet
    // "the whole board is warming up" cue that reads even without watching the
    // watermark or combo number.
    var padFl = state === 'playing' ? feverLevel(combo) : 0;
    var padBaseA = 0.13 + padFl * 0.05;
    for (var p = 0; p < LANES; p++) {
      var lit = state === 'playing' && (gameTime - laneFlash[p]) < 0.16;
      var hue = laneHue(p);
      var grad = ctx.createLinearGradient(0, padTop, 0, H);
      grad.addColorStop(0, hsl(hue, 72, 56, lit ? 0.46 : padBaseA));
      grad.addColorStop(1, hsl(hue, 72, 56, 0));
      ctx.fillStyle = grad;
      ctx.fillRect(p * laneW + 1, padTop, laneW - 2, padH);
      // key label — or, when Joker power-ups are on, the banked suit glyph
      // this lane fires (visually replaces the key letter while a suit is ready).
      // On touch input the keyboard letter is meaningless, so it's replaced by
      // a plain tap dot (isTouch, set once at boot from a pointer:coarse check).
      var su = jokerPower ? laneSuit(p) : -1;
      if (su >= 0 && suitQueue.indexOf(su) !== -1) {
        ctx.save();
        ctx.shadowColor = SUIT_COL[su]; ctx.shadowBlur = 14;
        ctx.fillStyle = '#fff7e1';
        ctx.font = '600 ' + Math.round(Math.min(laneW * 0.44, 34)) + "px 'JetBrains Mono', monospace";
        ctx.textAlign = 'center'; ctx.textBaseline = 'middle';
        ctx.fillText(SUIT_GLYPH[su], (p + 0.5) * laneW, strikeY + padH * (isTouch ? 0.52 : 0.40));
        ctx.restore();
        if (!isTouch) {
          ctx.fillStyle = 'rgba(255,247,225,0.6)';
          ctx.font = '600 ' + Math.round(Math.min(laneW * 0.15, 14)) + "px 'JetBrains Mono', monospace";
          ctx.textAlign = 'center'; ctx.textBaseline = 'middle';
          ctx.fillText(KEY_FOR_LANE[p].toUpperCase(), (p + 0.5) * laneW, strikeY + padH * 0.76);
        }
      } else if (isTouch) {
        var dotR = Math.max(3, Math.min(laneW * 0.06, 6));
        ctx.beginPath();
        ctx.fillStyle = lit ? 'rgba(241,236,250,0.9)' : hsl(hue, 62, 82, 0.55);
        ctx.arc((p + 0.5) * laneW, strikeY + padH * 0.55, dotR, 0, Math.PI * 2);
        ctx.fill();
      } else {
        ctx.fillStyle = lit ? 'rgba(241,236,250,0.85)' : hsl(hue, 62, 82, 0.5);
        ctx.font = '600 ' + Math.round(Math.min(laneW * 0.18, 18)) + "px 'JetBrains Mono', monospace";
        ctx.textAlign = 'center'; ctx.textBaseline = 'middle';
        ctx.fillText(KEY_FOR_LANE[p].toUpperCase(), (p + 0.5) * laneW, strikeY + padH * 0.55);
      }
    }
    // rainbow strike line — always
    var sg = ctx.createLinearGradient(0, 0, W, 0);
    for (var hp = 0; hp < LANES; hp++) sg.addColorStop((hp + 0.5) / LANES, hsl(laneHue(hp), 88, 64, 0.92));
    ctx.strokeStyle = sg;
    ctx.lineWidth = 2;
    ctx.beginPath(); ctx.moveTo(0, strikeY); ctx.lineTo(W, strikeY); ctx.stroke();

    // tiles
    var refT = state === 'playing' ? gameTime
             : state === 'countdown' ? -((countEnd - now) / 1000) : gameTime;
    for (var i = 0; i < tiles.length; i++) {
      var tl = tiles[i];
      // held sustain: bottom pinned at the strike line, top = the remaining tail
      // descending into it (glowing) — invites whammy presses until it clears
      if (tl.held) {
        var endT = tl.hitTime + tl.dur;
        if (refT >= endT) { tl.held = false; continue; }
        var sTop = strikeY - (endT - refT) * sp;
        drawTile(tl, sTop, strikeY, true);
        continue;
      }
      if (tl.state === 'hit') continue;
      var bottomY = strikeY - (tl.hitTime - refT) * sp;
      var len = Math.max(laneW * 0.55, tl.dur * sp);
      // never let a tile reach the next same-lane tile — leave a small seam so fast
      // runs read as separate keys instead of one overlapping blur
      if (tl.gapNext !== Infinity) {
        var cap = tl.gapNext * sp - 3;
        if (len > cap) len = Math.max(4, cap);
      }
      var topY = bottomY - len;
      if (bottomY < -4 || topY > H + 4) continue;
      // a long note about to be struck shows a "hold" hint ring at its head
      drawTile(tl, topY, bottomY);
    }

    // sparks (hit rings)
    for (var s = sparks.length - 1; s >= 0; s--) {
      var sk = sparks[s], age = gameTime - sk.t;
      if (age > 0.4) { sparks.splice(s, 1); continue; }
      var rr = 8 + age * 130, al = 1 - age / 0.4;
      ctx.globalAlpha = al * 0.8; ctx.strokeStyle = sk.col;
      ctx.lineWidth = 2.5;
      ctx.beginPath(); ctx.arc(sk.x, sk.y, rr, 0, Math.PI * 2); ctx.stroke();
      ctx.globalAlpha = 1;
    }

    // feedback pops
    for (var q = pops.length - 1; q >= 0; q--) {
      var po = pops[q], a2 = gameTime - po.t;
      if (a2 > 0.7) { pops.splice(q, 1); continue; }
      ctx.globalAlpha = 1 - a2 / 0.7;
      ctx.fillStyle = po.col;
      ctx.font = "600 14px 'JetBrains Mono', monospace";
      ctx.textAlign = 'center'; ctx.textBaseline = 'middle';
      ctx.fillText(po.txt, po.x, po.y - a2 * 34);
      ctx.globalAlpha = 1;
    }

    // big combo watermark — escalates through the fever tiers: brighter, warms
    // from crimson toward gold, gains a glow at tier 2+, and gets a slow pulse
    // at the top tier (gated behind reduceMotion like the rest of the FX).
    if (state === 'playing' && combo >= 5) {
      var wmFl = feverLevel(combo);
      var wmScale = (wmFl >= 3 && !reduceMotion) ? 1 + 0.04 * Math.sin(gameTime * 6) : 1;
      ctx.save();
      ctx.globalAlpha = 0.16 + wmFl * 0.05;
      ctx.fillStyle = wmFl >= 2 ? '#e8cf8c' : '#e0795f';
      if (wmFl >= 2) { ctx.shadowColor = ctx.fillStyle; ctx.shadowBlur = 26; }
      ctx.font = "500 " + Math.round(Math.min(W, H) * 0.26 * wmScale) + "px 'Lora', serif";
      ctx.textAlign = 'center'; ctx.textBaseline = 'middle';
      ctx.fillText(combo + '', W / 2, H * 0.4);
      ctx.restore();
    }

    // countdown digits
    if (state === 'countdown') {
      var secs = Math.ceil((countEnd - now) / 1000);
      ctx.fillStyle = 'rgba(217,185,106,0.92)';
      ctx.font = "500 " + Math.round(Math.min(W, H) * 0.34) + "px 'Lora', serif";
      ctx.textAlign = 'center'; ctx.textBaseline = 'middle';
      ctx.fillText(secs > 0 ? secs + '' : '✦', W / 2, H * 0.42);
    }

    // sheet-music strip (same clock as the falling tiles)
    drawStaff(refT);
  }

  function drawTile(tl, topY, bottomY, active) {
    var x = tl.lane * laneW + laneW * 0.10;
    var w = laneW * 0.80;
    var y = topY, h = bottomY - topY;
    var r = Math.min(10, w * 0.22, h * 0.45);
    var missed = tl.state === 'missed';
    var black = tl.black && !missed;   // sharp note → dark "black-key" bar
    var th = laneHue(tl.lane);
    var g = ctx.createLinearGradient(0, y, 0, bottomY);
    if (missed) {
      g.addColorStop(0, 'rgba(120,110,140,0.30)');
      g.addColorStop(1, 'rgba(90,80,110,0.18)');
    } else if (active) {
      // held sustain bar — bright pulsing lane hue to invite whammy presses
      var puls = 0.5 + 0.5 * Math.sin(gameTime * 22);
      g.addColorStop(0, hsl(th, 95, 60 + puls * 18, 0.30));
      g.addColorStop(1, hsl(th, 95, 70 + puls * 12, 0.95));
    } else {
      // rainbow lane-hue tiles (default visual identity); sharps darker shade,
      // naturals brighter — black-key cue is preserved without dropping out of palette
      if (black) {
        g.addColorStop(0, hsl(th, 55, 34, 0.97));
        g.addColorStop(1, hsl(th, 60, 21, 0.97));
      } else {
        g.addColorStop(0, hsl(th, 88, 67, 0.96));
        g.addColorStop(1, hsl(th, 82, 50, 0.94));
      }
    }
    if (active) { ctx.save(); ctx.shadowColor = hsl(th, 95, 65, 0.9); ctx.shadowBlur = 16; }
    roundRect(x, y, w, h, r);
    ctx.fillStyle = g; ctx.fill();
    if (!missed) {
      ctx.strokeStyle = active ? hsl(th, 95, 85, 0.95)
                      : (black ? hsl(th, 60, 52, 0.6) : 'rgba(255,255,255,0.6)');
      ctx.lineWidth = active ? 2 : 1; ctx.stroke();
    }
    if (active) ctx.restore();
  }

  // ── Sheet-music preview strip ───────────────────────────────────────
  // Slim treble staff above the playfield. Notes scroll right-to-left in sync
  // with the falling tiles; a fixed gold "now" line sits just after the key sig.
  // MIDI → staff position uses the song's keySig so true accidentals appear inline.
  function midiToStaff(midi, keySig) {
    var pc = ((midi % 12) + 12) % 12;
    var oct = Math.floor(midi / 12) - 1;
    // pc → [letter (0=C..6=B), accidental (-1/0/+1)] under sharp/flat spelling
    var sharpMap = [[0,0],[0,1],[1,0],[1,1],[2,0],[3,0],[3,1],[4,0],[4,1],[5,0],[5,1],[6,0]];
    var flatMap  = [[0,0],[1,-1],[1,0],[2,-1],[2,0],[3,0],[4,-1],[4,0],[5,-1],[5,0],[6,-1],[6,0]];
    var pair = keySig >= 0 ? sharpMap[pc] : flatMap[pc];
    var letter = pair[0], acc = pair[1];
    // accidental implied by key sig?
    var SHARP_LETTERS = [3, 0, 4, 1, 5, 2, 6]; // F C G D A E B
    var FLAT_LETTERS  = [6, 2, 5, 1, 4, 0, 3]; // B E A D G C F
    var implied = 0;
    if (keySig > 0) {
      for (var k = 0; k < keySig && k < 7; k++) if (SHARP_LETTERS[k] === letter) implied = 1;
    } else if (keySig < 0) {
      for (var kf = 0; kf < -keySig && kf < 7; kf++) if (FLAT_LETTERS[kf] === letter) implied = -1;
    }
    var accGlyph = '';
    if (acc !== implied) accGlyph = acc === 1 ? '♯' : (acc === -1 ? '♭' : '♮');
    return { diatonic: oct * 7 + letter, accidental: accGlyph };
  }

  function drawLedgers(ny, lineY, lineGap, nx) {
    var topY = lineY[0], botY = lineY[4], h = lineGap / 2;
    ctxS.strokeStyle = 'rgba(217,185,106,0.45)';
    ctxS.lineWidth = 1;
    if (ny < topY - h * 0.6) {
      for (var y = topY - lineGap; y > ny - h * 0.6; y -= lineGap) {
        ctxS.beginPath();
        ctxS.moveTo(nx - lineGap * 0.85, y); ctxS.lineTo(nx + lineGap * 0.85, y);
        ctxS.stroke();
      }
    } else if (ny > botY + h * 0.6) {
      for (var y2 = botY + lineGap; y2 < ny + h * 0.6; y2 += lineGap) {
        ctxS.beginPath();
        ctxS.moveTo(nx - lineGap * 0.85, y2); ctxS.lineTo(nx + lineGap * 0.85, y2);
        ctxS.stroke();
      }
    }
  }

  function drawStaff(refT) {
    ctxS.clearRect(0, 0, WS, HS);
    // Bail early when there's nothing scrolling — saves all the chrome work in
    // the menu / pause / end / game-over states.
    if (state !== 'playing' && state !== 'countdown' && state !== 'paused') return;
    // Staff lives near the TOP of the playfield so tiles fall through it on their
    // way to the strike line. Deliberately thin — a quiet score behind the tiles,
    // not a focal point.
    var staffTop = HS * 0.09, staffBot = HS * 0.16;
    var lineGap = (staffBot - staffTop) / 4;
    if (lineGap < 6) return;   // too short to read — skip rather than smudge
    var lineY = [];
    for (var i = 0; i < 5; i++) lineY.push(staffTop + i * lineGap);
    // Diatonic numbering: 7·oct + letter; bottom line (E4) = 30, middle (B4) = 34, top (F5) = 38.
    function diaY(d) { return staffBot - (d - 30) * (lineGap / 2); }

    // Treble clef glyph (Unicode G clef) — sized to span the staff
    ctxS.fillStyle = 'rgba(232,207,140,0.62)';
    ctxS.font = Math.round(lineGap * 5.2) + "px 'Lora', serif";
    ctxS.textAlign = 'center'; ctxS.textBaseline = 'middle';
    var clefX = lineGap * 1.4;
    ctxS.fillText('𝄞', clefX, (staffTop + staffBot) / 2 + lineGap * 0.15);

    // Key signature
    var SHARP_DIA = [38, 35, 39, 37, 33, 36, 34]; // F# C# G# D# A# E# B#
    var FLAT_DIA  = [34, 36, 33, 37, 32, 35, 31]; // Bb Eb Ab Db Gb Cb Fb
    ctxS.fillStyle = 'rgba(217,185,106,0.62)';
    ctxS.font = Math.round(lineGap * 2.6) + "px 'Lora', serif";
    ctxS.textAlign = 'left'; ctxS.textBaseline = 'middle';
    var sigX = clefX + lineGap * 1.6, sigStep = lineGap * 0.85;
    if (staffKeySig > 0) {
      for (var ks = 0; ks < staffKeySig && ks < 7; ks++) {
        ctxS.fillText('♯', sigX, diaY(SHARP_DIA[ks]));
        sigX += sigStep;
      }
    } else if (staffKeySig < 0) {
      for (var kf = 0; kf < -staffKeySig && kf < 7; kf++) {
        ctxS.fillText('♭', sigX, diaY(FLAT_DIA[kf]));
        sigX += sigStep;
      }
    }

    // 5 staff lines — extra-subtle since they sit under tiles
    ctxS.strokeStyle = 'rgba(217,185,106,0.22)';
    ctxS.lineWidth = 1;
    for (var li = 0; li < 5; li++) {
      ctxS.beginPath(); ctxS.moveTo(0, lineY[li]); ctxS.lineTo(WS, lineY[li]); ctxS.stroke();
    }

    // "Now" line — bright gold vertical, the navigational anchor
    var nowX = Math.max(lineGap * 4.6, sigX + lineGap * 0.6);
    ctxS.strokeStyle = 'rgba(232,207,140,0.78)';
    ctxS.lineWidth = 2;
    ctxS.beginPath();
    ctxS.moveTo(nowX, staffTop - lineGap * 0.6);
    ctxS.lineTo(nowX, staffBot + lineGap * 0.6);
    ctxS.stroke();

    // Scrolling notes (clipped to area right of the clef+sig block)
    var visibleW = WS - nowX - lineGap * 0.4;
    var beatsAhead = 6;                                  // ~6 beats lookahead
    var pxPerBeat = visibleW / beatsAhead;
    var pxPerSec = pxPerBeat / staffSpb;

    ctxS.save();
    ctxS.beginPath();
    ctxS.rect(nowX - lineGap * 1.6, 0, WS - (nowX - lineGap * 1.6), HS);
    ctxS.clip();

    var accFontPx = Math.round(lineGap * 1.9);
    ctxS.font = accFontPx + "px 'Lora', serif";
    ctxS.textAlign = 'center'; ctxS.textBaseline = 'middle';

    // Time-window cull: a note is on-screen only while its start time falls
    // between (refT - left margin) and (refT + right margin). Computed once so
    // every off-screen note skips with one cheap compare, not a position calc.
    var leftMargin  = (lineGap * 1.4) / pxPerSec;
    var rightMargin = (WS - nowX + lineGap) / pxPerSec;
    var tMin = refT - leftMargin, tMax = refT + rightMargin;

    for (var ni = 0; ni < staffNotes.length; ni++) {
      var sn = staffNotes[ni];
      if (sn.start < tMin || sn.start > tMax) continue;
      var nx = nowX + (sn.start - refT) * pxPerSec;
      var inf = midiToStaff(sn.midi, staffKeySig);
      var ny = diaY(inf.diatonic);
      drawLedgers(ny, lineY, lineGap, nx);
      if (inf.accidental) {
        ctxS.fillStyle = 'rgba(232,207,140,0.78)';
        ctxS.fillText(inf.accidental, nx - lineGap * 1.0, ny);
      }
      // Real notation glyph: notehead (hollow/filled) + stem + flag(s) per duration.
      drawNoteGlyph(nx, ny, sn.durBeats, inf.diatonic, lineGap);
    }
    ctxS.restore();
  }

  // Render one note in proper engraved form. durBeats decides the rhythmic class;
  // diatonic decides stem direction (below middle line → stem up, on/above → down).
  function drawNoteGlyph(nx, ny, durBeats, diatonic, lineGap) {
    var hollow, hasStem, flags;
    if (durBeats >= 4)        { hollow = true;  hasStem = false; flags = 0; } // whole
    else if (durBeats >= 2)   { hollow = true;  hasStem = true;  flags = 0; } // half
    else if (durBeats >= 1)   { hollow = false; hasStem = true;  flags = 0; } // quarter
    else if (durBeats >= 0.5) { hollow = false; hasStem = true;  flags = 1; } // eighth
    else                      { hollow = false; hasStem = true;  flags = 2; } // sixteenth

    var rw = lineGap * 0.62, rh = lineGap * 0.48;
    var inkFill = 'rgba(247,241,230,0.95)';
    var inkLine = 'rgba(247,241,230,0.92)';

    // notehead (slight engraving tilt). No shadowBlur — it's the single most
    // expensive canvas op on mobile (forces software rendering on weaker GPUs).
    // The bright ink + dark playfield gradient give enough contrast on its own.
    ctxS.save();
    ctxS.translate(nx, ny);
    ctxS.rotate(-0.32);
    ctxS.beginPath();
    ctxS.ellipse(0, 0, rw, rh, 0, 0, Math.PI * 2);
    if (hollow) {
      ctxS.strokeStyle = inkLine; ctxS.lineWidth = 1.8; ctxS.stroke();
    } else {
      ctxS.fillStyle = inkFill; ctxS.fill();
    }
    ctxS.restore();

    if (!hasStem) return;

    // stem — up for notes below the middle line (B4=34), down for on/above.
    // Anchor at the side of the notehead per standard engraving.
    var stemUp = diatonic < 34;
    var stemLen = lineGap * 3.3;
    var stemX = nx + (stemUp ? rw * 0.78 : -rw * 0.78);
    var stemFar = ny + (stemUp ? -stemLen : stemLen);
    ctxS.save();
    ctxS.strokeStyle = inkLine;
    ctxS.lineWidth = 1.4;
    ctxS.lineCap = 'round';
    ctxS.beginPath();
    ctxS.moveTo(stemX, ny + (stemUp ? -rh * 0.2 : rh * 0.2));
    ctxS.lineTo(stemX, stemFar);
    ctxS.stroke();

    // flags — curve away from the stem-side, repeated for sixteenths
    if (flags > 0) {
      var dir = stemUp ? 1 : -1;        // +1 = stem-up flag curves DOWN; −1 reverses
      var flagW = lineGap * 1.05;
      var flagH = lineGap * 1.05;
      ctxS.lineWidth = 1.7;
      for (var f = 0; f < flags; f++) {
        var fy = stemFar + dir * f * lineGap * 0.55;
        ctxS.beginPath();
        ctxS.moveTo(stemX, fy);
        ctxS.bezierCurveTo(
          stemX + flagW * 0.55, fy + flagH * 0.25 * dir,
          stemX + flagW * 0.95, fy + flagH * 0.55 * dir,
          stemX + flagW * 0.55, fy + flagH * 1.05 * dir
        );
        ctxS.stroke();
      }
    }
    ctxS.restore();
  }

  function roundRect(x, y, w, h, r) {
    ctx.beginPath();
    ctx.moveTo(x + r, y);
    ctx.arcTo(x + w, y, x + w, y + h, r);
    ctx.arcTo(x + w, y + h, x, y + h, r);
    ctx.arcTo(x, y + h, x, y, r);
    ctx.arcTo(x, y, x + w, y, r);
    ctx.closePath();
  }
  function rgba(hex, a) {
    var n = parseInt(hex.slice(1), 16);
    return 'rgba(' + (n >> 16 & 255) + ',' + (n >> 8 & 255) + ',' + (n & 255) + ',' + a + ')';
  }

