  // ── Persistent per-song best scores + saved options ─────────────────
  // Bests are tracked PER SETTINGS, not one absolute number per song: Keys (4-7),
  // Speed, and Joker all change how hard a run is (Keys already carries a
  // KEY_SCORE_MULT, Joker's ×2/heal/shield make its scores incomparable outright),
  // so a score set on Gentle/4-key shouldn't silently overwrite — or look like —
  // a benchmark for Swift/7-key. Shape: { songName: { settingsKey: {score, stars} } } —
  // stars is the star rating (0-3, same scale as the end screen) EARNED ON the run
  // that set that score, so the menu can show "your best" as one coherent pair
  // rather than two figures that could be from different attempts.
  var bests = {};                     // song name -> { settingsKey -> {score, stars} }
  function settingsKey(lanes, diff, joker) {
    return lanes + '_' + diff + (joker ? '_j' : '');
  }
  function loadBests() {
    try { bests = JSON.parse(localStorage.getItem('ckeys-bests')) || {}; } catch (e) { bests = {}; }
    // Two migration layers, applied in order, so any prior localStorage shape
    // lands on the current one:
    //  1. old flat { songName: number } (pre-per-settings tracking) → filed
    //     under the 7-key/Steady/no-Joker bucket, the default nearly everyone
    //     was playing under before that existed.
    //  2. { songName: { settingsKey: number } } (pre-stars tracking) → wrapped
    //     into { score: number, stars: 0 } — the star rating for those old
    //     records is genuinely unknown, so it shows as unrated until replayed,
    //     rather than guessed.
    var migrated = false;
    for (var name in bests) {
      if (!bests.hasOwnProperty(name)) continue;
      if (typeof bests[name] === 'number') {
        bests[name] = { '7_1': bests[name] };
        migrated = true;
      }
      var rec = bests[name];
      for (var sk in rec) {
        if (rec.hasOwnProperty(sk) && typeof rec[sk] === 'number') {
          rec[sk] = { score: rec[sk], stars: 0 };
          migrated = true;
        }
      }
    }
    if (migrated) saveBests();
  }
  function bestRecordFor(name, lanes, diff, joker) {
    var rec = bests[name];
    var r = rec && rec[settingsKey(lanes, diff, joker)];
    return r || { score: 0, stars: 0 };
  }
  function bestFor(name, lanes, diff, joker) {
    return bestRecordFor(name, lanes, diff, joker).score;
  }
  function saveBests() {
    try { localStorage.setItem('ckeys-bests', JSON.stringify(bests)); } catch (e) {}
  }
  // ── Ouroboros unlock — any song 3-starred, on any settings, unlocks a
  // secret row in Settings. Scans `bests` fresh each time rather than tracking
  // a separate persisted flag: `bests` already carries the star-rating maxima
  // (see endRun), so this stays correct for free as new records come in, and
  // for existing players the moment loadBests() runs on their saved data. ──
  function hasThreeStarSong() {
    for (var name in bests) {
      if (!bests.hasOwnProperty(name)) continue;
      var rec = bests[name];
      for (var sk in rec) {
        if (rec.hasOwnProperty(sk) && rec[sk] && rec[sk].stars >= 3) return true;
      }
    }
    return false;
  }
  function updateSnakeUnlock() {
    var row = document.getElementById('snakeRow');
    if (row) row.style.display = hasThreeStarSong() ? '' : 'none';
  }
  // Speed + number-of-keys + per-count layout choices, remembered between plays.
  function loadPrefs() {
    try {
      var p = JSON.parse(localStorage.getItem('ckeys-prefs')) || {};
      if (typeof p.diff === 'number' && DIFFS[p.diff]) chosenDiff = p.diff;
      if (p.layouts && typeof p.layouts === 'object') {
        [4, 5, 6, 7].forEach(function (c) {
          var v = p.layouts[c];
          if (typeof v === 'number' && KEY_LAYOUTS_BY_COUNT[c][v]) chosenLayouts[c] = v;
        });
      }
      if (typeof p.lanes === 'number' && LANE_OPTS[p.lanes]) setLaneCount(p.lanes);
      if (p.joker) jokerPower = true;
    } catch (e) {}
    applyLayout();
    buildLayoutSeg();
    syncSeg('#diffSeg', 'diff', chosenDiff);
    syncSeg('#keysSeg', 'lanes', chosenLaneIdx);
    var jb = document.getElementById('jokerBtn');
    if (jb) jb.setAttribute('aria-pressed', jokerPower ? 'true' : 'false');
    updateSuits();
  }
  function savePrefs() {
    try { localStorage.setItem('ckeys-prefs', JSON.stringify({ diff: chosenDiff, lanes: chosenLaneIdx, layouts: chosenLayouts, joker: jokerPower })); } catch (e) {}
  }
